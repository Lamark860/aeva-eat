-- Идентичность места — это физическая точка, а не только имя бренда.
-- Старый idx_places_name_city (008) ключевался лишь по (name, city), поэтому
-- второй филиал сети в одном городе («Волконский в Кремле» при уже заведённом
-- «Волконском» в Москве) конфликтовал и не мог быть создан. Добавляем адрес в
-- ключ, чтобы разные филиалы сосуществовали, а настоящий дубль (то же
-- название + тот же адрес + тот же город) по-прежнему ловился.
--
-- Безопасность миграции: новый ключ — строгое надмножество старого (добавлен
-- только address), поэтому любые строки, удовлетворявшие UNIQUE(name, city),
-- остаются уникальными и по UNIQUE(name, address, city). Создание индекса НЕ
-- может упасть на данных, прошедших 008. Нормализацию намеренно оставляем
-- такой же (LOWER, без TRIM) — агрессивная нормализация (TRIM/схлопывание
-- пробелов) потребовала бы предварительного дедуп-прохода и здесь не делается.
DROP INDEX IF EXISTS idx_places_name_city;
CREATE UNIQUE INDEX IF NOT EXISTS idx_places_identity
    ON places (LOWER(name), LOWER(COALESCE(address, '')), LOWER(COALESCE(city, '')));
